package Modelo;

/**
 *
 * @author drago
 */
public class configuracion {
    private int idConfiguracion;
    private int maxEjemplaresPrestables;
    private double moraDiaria;
    private int anioMoraDiaria;
    // Getters y setters

    public int getIdConfiguracion() {
        return idConfiguracion;
    }

    public void setIdConfiguracion(int idConfiguracion) {
        this.idConfiguracion = idConfiguracion;
    }

    public int getMaxEjemplaresPrestables() {
        return maxEjemplaresPrestables;
    }

    public void setMaxEjemplaresPrestables(int maxEjemplaresPrestables) {
        this.maxEjemplaresPrestables = maxEjemplaresPrestables;
    }

    public double getMoraDiaria() {
        return moraDiaria;
    }

    public void setMoraDiaria(double moraDiaria) {
        this.moraDiaria = moraDiaria;
    }

    public int getAnioMoraDiaria() {
        return anioMoraDiaria;
    }

    public void setAnioMoraDiaria(int anioMoraDiaria) {
        this.anioMoraDiaria = anioMoraDiaria;
    }
    
    
}
